const SidebarMenuButton = React.forwardRef<
  HTMLButtonElement,
  Omit<React.ComponentProps<typeof Button>, "variant" | "icon"> & {
    isActive?: boolean;
    tooltip?: string | React.ComponentProps<typeof TooltipContent>;
  }
>(({ isActive = false, tooltip, className, ...props }, ref) => {
  const { isMobile, state } = useSidebar();

  const button = (
    <Button
      ref={ref}
      variant={isActive ? "solid" : "soft"}
      className={cn(
        "w-full justify-start gap-2 whitespace-normal p-2 [&>span:last-child]:truncate [&>svg]:size-5 [&>svg]:shrink-0",
        className,
      )}
      {...props}
    />
  );

  if (!tooltip) {
    return button;
  }

  if (typeof tooltip === "string") {
    tooltip = {
      children: tooltip,
    };
  }

  return (
    <Tooltip>
      <TooltipTrigger asChild>{button}</TooltipTrigger>
      <TooltipContent
        sideOffset={8}
        side="right"
        align="center"
        hidden={state !== "collapsed" || isMobile}
        {...tooltip}
      />
    </Tooltip>
  );
});
SidebarMenuButton.displayName = "SidebarMenuButton";

